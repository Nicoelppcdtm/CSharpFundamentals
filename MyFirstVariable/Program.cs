// Declare a variable
string name;

// Write() does not return to next line, WriteLine() does
Console.Write("Enter your name: ");

// ReadLine() takes text input from the keyboard until the user hits <enter> / <return>
name = Console.ReadLine();

// Adding two strings (+) concatenates them
Console.WriteLine("Hello, " + name);