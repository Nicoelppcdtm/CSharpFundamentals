// escape characters for literal quotes in string data
Console.WriteLine("The cow says, \"Moo!\"");

// tab and new line escape codes
Console.WriteLine("\tTab");
Console.WriteLine("\t\t\tThree Tabs");
Console.WriteLine("How about a \nNew Line?");

// adding strings is concatonation
Console.WriteLine("15" + "20");

// Write() doesn't add a new line automatically like WriteLine() does
Console.Write("One ");
Console.Write("Two ");
